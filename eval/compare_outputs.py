"""
compare_outputs.py — Head-to-Head Comparison: Project Trinetra vs Google Gemini LLM
🔱 Evaluates both rankings against the benchmark gold labels and prints a detailed scorecard.

Expects:
  - eval/trinetra_benchmark_output.csv (Generated by Trinetra)
  - eval/llm_output.json (Pasted by user from Gemini)
"""

import json
import csv
import os
import sys
import math
from pathlib import Path

# Force UTF-8 on Windows
if sys.platform.startswith("win"):
    try:
        sys.stdout.reconfigure(encoding="utf-8")
        sys.stderr.reconfigure(encoding="utf-8")
    except AttributeError:
        pass

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT))
sys.path.insert(0, str(ROOT / "src"))

GOLD_PATH = ROOT / "eval" / "gold_auto.csv"
TRINETRA_OUT = ROOT / "eval" / "trinetra_benchmark_output.csv"
LLM_OUT = ROOT / "eval" / "llm_output.json"


def load_gold_labels(path) -> dict[str, float]:
    gold = {}
    with open(path, encoding="utf-8") as f:
        for row in csv.DictReader(f):
            gold[row["candidate_id"].strip()] = float(row["tier"])
    return gold


def load_trinetra_ranking(path) -> list[str]:
    ranking = []
    with open(path, encoding="utf-8") as f:
        for row in csv.DictReader(f):
            ranking.append((int(row["rank"]), row["candidate_id"].strip()))
    ranking.sort()
    return [cid for _, cid in ranking]


def dcg(relevances: list[float]) -> float:
    return sum((2 ** rel - 1) / math.log2(i + 2) for i, rel in enumerate(relevances))


def compute_metrics(ranked_ids: list[str], gold: dict[str, float], k: int) -> dict:
    sliced_ids = ranked_ids[:k]
    rels = [gold.get(cid, 0.0) for cid in sliced_ids]
    
    # NDCG
    actual_dcg = dcg(rels)
    ideal_list = sorted(gold.values(), reverse=True)
    ideal_dcg = dcg(ideal_list[:k])
    ndcg = actual_dcg / ideal_dcg if ideal_dcg > 0 else 0.0
    
    # Precision@k (relevance >= 3)
    relevant_count = sum(1 for r in rels if r >= 3.0)
    precision = relevant_count / len(sliced_ids) if sliced_ids else 0.0
    
    return {
        "ndcg": ndcg,
        "precision": precision,
        "relevant_hits": relevant_count
    }


def main():
    print()
    print("  ====================================================")
    print("  🔱 HEAD-TO-HEAD BATTLE: TRINETRA VS GEMINI LLM")
    print("  ====================================================")
    
    if not os.path.exists(TRINETRA_OUT):
        print(f"  ERROR: Trinetra output not found at {TRINETRA_OUT}")
        sys.exit(1)
    if not os.path.exists(LLM_OUT):
        print(f"  ERROR: LLM output not found at {LLM_OUT}. Please paste the LLM json response and save to eval/llm_output.json")
        sys.exit(1)
        
    # Load gold labels & results
    gold = load_gold_labels(GOLD_PATH)
    trinetra_rank = load_trinetra_ranking(TRINETRA_OUT)
    
    with open(LLM_OUT, "r", encoding="utf-8") as f:
        llm_data = json.load(f)
        
    llm_flagged = set(llm_data.get("flagged_honeypots", []))
    llm_top20 = [item["candidate_id"] for item in llm_data.get("top_20", [])]
    
    # Identify actual honeypots in the benchmark candidates
    benchmark_json = ROOT / "eval" / "benchmark_candidates.json"
    with open(benchmark_json, "r", encoding="utf-8") as f:
        benchmark_candidates = json.load(f)
        
    from src.guard_gate import run_guard_gate
    actual_honeypots = set()
    for c in benchmark_candidates:
        res = run_guard_gate(c)
        if res["is_hard_honeypot"]:
            actual_honeypots.add(c["candidate_id"])
            
    # ── 1. HONEYPOT FORENSICS COMPARISON ──
    print("  [1/3] HONEYPOT DETECTIVE WORK")
    print("  " + "-" * 40)
    
    # Trinetra honeypot detection
    trinetra_flagged = set()
    for c in benchmark_candidates:
        res = run_guard_gate(c)
        if res["is_hard_honeypot"]:
            trinetra_flagged.add(c["candidate_id"])
            
    # Compute stats for Trinetra
    t_true_pos = len(trinetra_flagged.intersection(actual_honeypots))
    t_false_pos = len(trinetra_flagged - actual_honeypots)
    t_fn = len(actual_honeypots - trinetra_flagged)
    t_recall = t_true_pos / len(actual_honeypots) if actual_honeypots else 1.0
    t_precision = t_true_pos / len(trinetra_flagged) if trinetra_flagged else 1.0
    
    # Compute stats for LLM
    l_true_pos = len(llm_flagged.intersection(actual_honeypots))
    l_false_pos = len(llm_flagged - actual_honeypots)
    l_fn = len(actual_honeypots - llm_flagged)
    l_recall = l_true_pos / len(actual_honeypots) if actual_honeypots else 1.0
    l_precision = l_true_pos / len(llm_flagged) if llm_flagged else 1.0
    
    # Check for honeypot leakage into Top 20
    t_leak = sum(1 for cid in trinetra_rank[:20] if cid in actual_honeypots)
    l_leak = sum(1 for cid in llm_top20[:20] if cid in actual_honeypots)
    
    print(f"  Total actual honeypots in dataset: {len(actual_honeypots)}")
    print()
    print(f"  Trinetra Fraud Detection:")
    print(f"    - Flagged: {len(trinetra_flagged)} | True Pos: {t_true_pos} | False Pos: {t_false_pos}")
    print(f"    - Fraud Recall:    {t_recall:.1%}")
    print(f"    - Fraud Precision: {t_precision:.1%}")
    print(f"    - Honeypots leaked into Top 20: {t_leak}")
    print()
    print(f"  Gemini LLM Fraud Detection:")
    print(f"    - Flagged: {len(llm_flagged)} | True Pos: {l_true_pos} | False Pos: {l_false_pos}")
    print(f"    - Fraud Recall:    {l_recall:.1%}")
    print(f"    - Fraud Precision: {l_precision:.1%}")
    print(f"    - Honeypots leaked into Top 20: {l_leak}")
    print()
    
    # ── 2. RANKING QUALITY COMPARISON (Top 10 and Top 20) ──
    print("  [2/3] RANKING METRICS (NDCG & PRECISION)")
    print("  " + "-" * 40)
    
    t_metrics_10 = compute_metrics(trinetra_rank, gold, k=10)
    l_metrics_10 = compute_metrics(llm_top20, gold, k=10)
    
    t_metrics_20 = compute_metrics(trinetra_rank, gold, k=20)
    l_metrics_20 = compute_metrics(llm_top20, gold, k=20)
    
    print(f"  Top 10 Metrics:")
    print(f"    - Trinetra NDCG@10: {t_metrics_10['ndcg']:.4f}  |  Gemini NDCG@10: {l_metrics_10['ndcg']:.4f}")
    print(f"    - Trinetra P@10:    {t_metrics_10['precision']:.1%}  |  Gemini P@10:    {l_metrics_10['precision']:.1%}")
    print()
    print(f"  Top 20 Metrics:")
    print(f"    - Trinetra NDCG@20: {t_metrics_20['ndcg']:.4f}  |  Gemini NDCG@20: {l_metrics_20['ndcg']:.4f}")
    print(f"    - Trinetra P@20:    {t_metrics_20['precision']:.1%}  |  Gemini P@20:    {l_metrics_20['precision']:.1%}")
    print()
    
    # ── 3. DETAILED WINNER VERDICT ──
    print("  [3/3] FINAL CHAMPIONSHIP VERDICT")
    print("  " + "-" * 40)
    
    points_t = 0
    points_l = 0
    
    # Rule 1: Fraud Leakage
    if t_leak < l_leak:
        print("  ✓ Trust Safety: Trinetra WINS (fewer honeypots leaked)")
        points_t += 2
    elif l_leak < t_leak:
        print("  ✓ Trust Safety: Gemini WINS (fewer honeypots leaked)")
        points_l += 2
    else:
        print("  ✓ Trust Safety: TIE (both prevented leakage)")
        
    # Rule 2: NDCG@10
    if t_metrics_10['ndcg'] > l_metrics_10['ndcg'] + 0.01:
        print(f"  ✓ Top-10 Ranking: Trinetra WINS (+{t_metrics_10['ndcg'] - l_metrics_10['ndcg']:.4f} NDCG)")
        points_t += 2
    elif l_metrics_10['ndcg'] > t_metrics_10['ndcg'] + 0.01:
        print(f"  ✓ Top-10 Ranking: Gemini WINS (+{l_metrics_10['ndcg'] - t_metrics_10['ndcg']:.4f} NDCG)")
        points_l += 2
    else:
        print("  ✓ Top-10 Ranking: TIE (NDCG matches within noise)")
        
    # Rule 3: NDCG@20
    if t_metrics_20['ndcg'] > l_metrics_20['ndcg'] + 0.01:
        print(f"  ✓ Top-20 List Depth: Trinetra WINS (+{t_metrics_20['ndcg'] - l_metrics_20['ndcg']:.4f} NDCG)")
        points_t += 1
    elif l_metrics_20['ndcg'] > t_metrics_20['ndcg'] + 0.01:
        print(f"  ✓ Top-20 List Depth: Gemini WINS (+{l_metrics_20['ndcg'] - t_metrics_20['ndcg']:.4f} NDCG)")
        points_l += 1
    else:
        print("  ✓ Top-20 List Depth: TIE")
        
    # Rule 4: Fraud Detection Precision/Recall
    if t_recall > l_recall:
        print("  ✓ Fraud Recall: Trinetra WINS (caught more total honeypots)")
        points_t += 1
    elif l_recall > t_recall:
        print("  ✓ Fraud Recall: Gemini WINS (caught more total honeypots)")
        points_l += 1
        
    print()
    if points_t > points_l:
        print(f"  🏆 OVERALL CHAMPION: PROJECT TRINETRA WINS ({points_t} vs {points_l} points)!")
    elif points_l > points_t:
        print(f"  🏆 OVERALL CHAMPION: GEMINI LLM WINS ({points_l} vs {points_t} points)!")
    else:
        print("  🏆 OVERALL CHAMPION: DRAW!")
    print()


if __name__ == "__main__":
    main()
